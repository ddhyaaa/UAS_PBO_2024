    private void Tampil() {
        
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM bunga ORDER BY id");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("id");
            model.addColumn("nama");
            model.addColumn("warna");
            model.addColumn("asal");
            model.addColumn("harga");
            model.addColumn("stok");
            model.addColumn("jenis");
           
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            while (rs.next()){
                Object [] data = {rs.getString("id"), rs.getString("nama"), rs.getString("warna"), rs.getString("asal"), rs.getString("harga"), rs.getString("stok"), rs.getString("jenis")   };
                model.addRow(data);
                tblData.setModel(model);
            }
            
        }catch (Exception e){  
        }
    }
    
