    private void checkoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutActionPerformed
        if (selectedID !=null){
            try{
                if (jumlah.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Jumlah tidak boleh kosong");
                    return;
                }
                
                st = cn.createStatement();
                int beli = Integer.parseInt(jumlah.getText());
                
                if (beli > selectedStok){
                    JOptionPane.showMessageDialog(null, "Stok tidak mencukupi");
                    return;
                }
                int stokbaru = selectedStok - beli;
                String sql = "UPDATE bunga SET stok='"+stokbaru+"' WHERE id='"+selectedID+"'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Berhasil Checkout!");
                Tampil();
                bersih();
                
            }catch (SQLException e){
                 JOptionPane.showMessageDialog(null, "SQL ERROR");
                e.printStackTrace();
            }catch (Exception e){
                 JOptionPane.showMessageDialog(null, "ERROR");
                 e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "terjadi kesalahab");
        }
    }//GEN-LAST:event_checkoutActionPerformed

